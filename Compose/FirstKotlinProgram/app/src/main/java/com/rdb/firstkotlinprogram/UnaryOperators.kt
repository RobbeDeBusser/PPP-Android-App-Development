package com.rdb.firstkotlinprogram

fun main(args: Array<String>) {

    var number1:Double = 8.4
    var isCheck:Boolean = true

    println("+number = ${+number1}")
    println("-number = ${-number1}")
    println("++number = ${++number1}")
    println("--number = ${--number1}")
    println("!isCheck = ${!isCheck}")

}